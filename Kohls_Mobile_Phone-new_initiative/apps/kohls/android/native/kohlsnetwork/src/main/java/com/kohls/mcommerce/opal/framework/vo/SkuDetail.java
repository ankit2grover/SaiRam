
package com.kohls.mcommerce.opal.framework.vo;

/**
 * Model to hold SKU Details
 * 
 * @author nalin.k
 */
public class SkuDetail {

    private String skuCode;

    private String skuQuantity;

    private String skuPrice;

    private String skuStoreNum;

    private String skuAvailability;

    private String skuAvailableStock;

    private String skuAllocatedStock;

    public String getSkuCode() {
        return skuCode;
    }

    public void setSkuCode(String skuCode) {
        this.skuCode = skuCode;
    }

    public String getSkuQuantity() {
        return skuQuantity;
    }

    public void setSkuQuantity(String skuQuantity) {
        this.skuQuantity = skuQuantity;
    }

    public String getSkuPrice() {
        return skuPrice;
    }

    public void setSkuPrice(String skuPrice) {
        this.skuPrice = skuPrice;
    }

    public String getSkuStoreNum() {
        return skuStoreNum;
    }

    public void setSkuStoreNum(String skuStoreNum) {
        this.skuStoreNum = skuStoreNum;
    }

    public String getSkuAvailability() {
        return skuAvailability;
    }

    public void setSkuAvailability(String skuAvailability) {
        this.skuAvailability = skuAvailability;
    }

    public String getSkuAvailableStock() {
        return skuAvailableStock;
    }

    public void setSkuAvailableStock(String skuAvailableStock) {
        this.skuAvailableStock = skuAvailableStock;
    }

    public String getSkuAllocatedStock() {
        return skuAllocatedStock;
    }

    public void setSkuAllocatedStock(String skuAllocatedStock) {
        this.skuAllocatedStock = skuAllocatedStock;
    }

}
